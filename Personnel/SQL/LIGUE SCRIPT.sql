CREATE TABLE ligue(
ID_LIGUE INT,
NOM_LIGUE VARCHAR(255),
ADMIN_LIGUE INT UNIQUE,
PRIMARY KEY (ID_LIGUE),
FOREIGN KEY (ADMIN_LIGUE) REFERENCES employe(ID_EMPLOYE)
);

CREATE TABLE employe(
ID_EMPLOYE INT,
NOM_EMPLOYE VARCHAR(15),
TYPE_EMPLOYE VARCHAR(15),
PRENOM_EMPLOYE VARCHAR(15),
MAIL_EMPLOYE VARCHAR(15),
PASSWORD VARCHAR(15),
DATE_ARRIVE TIMESTAMP,
DATE_DEPART TIMESTAMP,
EST_ROOT BOOLEAN,
ID_LIGUE INT,
PRIMARY KEY (ID_EMPLOYE),
FOREIGN KEY (ID_LIGUE) REFERENCES ligue(ID_LIGUE)
);
