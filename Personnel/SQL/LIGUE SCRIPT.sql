CREATE TABLE ligue(
id_ligue INT AUTO_INCREMENT,
nom VARCHAR(255),
ADMIN_LIGUE INT UNIQUE,
PRIMARY KEY (id_ligue)
);

CREATE TABLE employe(
ID_EMPLOYE INT AUTO_INCREMENT,
nom VARCHAR(70),
prenom VARCHAR(70),
mail VARCHAR(100),
password VARCHAR(50),
date_arrive TIMESTAMP,
date_depart TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
root BOOLEAN,
ligue INT,
PRIMARY KEY (ID_EMPLOYE)
);

ALTER TABLE employe
ADD CONSTRAINT fk_ligue
FOREIGN KEY (ligue) REFERENCES ligue(id_ligue);

ALTER TABLE ligue
ADD CONSTRAINT fk_employe
FOREIGN KEY (ADMIN_LIGUE) REFERENCES employe(ID_EMPLOYE);